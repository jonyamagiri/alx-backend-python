#!/usr/bin/env python3
""" module 5-sum_list.py """
from typing import List


def sum_list(input_list: List[float]) -> float:
    """Return the sum of a given list of float numbers.
    Args:
        input_list: A list of float numbers.
    Returns:
        The sum of input_list as a float.
    """
    return sum(input_list)
