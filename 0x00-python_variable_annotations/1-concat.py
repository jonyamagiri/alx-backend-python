#!/usr/bin/env python3
""" module 1-concat.py """


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings into a new string.
    Args:
        str1: A string to be concatenated.
        str2: A string to be concatenated.
    Returns:
        A new string that is the concatenation of str1 and str2.

    """
    return str1 + str2
